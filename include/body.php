				<div class="body_left_main_text">
					<div class="text_center"><p>
						The Pfizer/BioNTech jab is the fastest vaccine to go from concept to reality, taking only 10 months to follow the same steps that normally span 10 years.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						The UK has already ordered 40 million doses of the jab - enough to vaccinate 20 million people.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						The doses will be rolled out as quickly as they can be made by Pfizer in Belgium, Mr Hancock said, with the first load next week and then "several millions" throughout December.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Scottish First Minister Nicola Sturgeon said the first people in Scotland will be immunised on Tuesday.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Welsh Health and Social Care Minister Vaughan Gething said the rollout of the Pfizer jab to care homes would be particularly difficult because of how it needs to be stored.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Mr Gething said that it was not possible to transport the Pfizer vaccine to more than 1,000 care homes across Wales.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						The bulk of the rollout across the UK will be next year, Mr Hancock said, adding: "2020 has been just awful and 2021 is going to be better."</p>
					</div>
				</div>
