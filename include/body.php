					<div class="body_left_main_text">
					<div class="text_center"><p>
						NHS England chief executive Sir Simon Stevens said the health service was preparing for "the largest-scale vaccination campaign in our country's history".</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						But experts said people still need to remain vigilant and follow rules to stop the virus spreading - including with social distancing, face masks and self-isolation.</p>
					</div>
				</div>
							<div class="body_left_main_text">
					<div class="text_center"><p>
						"We can't lower our guard yet," said the government's chief medical adviser Prof Chris Whitty.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						The order in which people will get the jab is recommended by the Joint Committee on Vaccinations and Immunisations and decided by the government.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Mass immunisation of everyone over 50, as well as younger people with pre-existing health conditions, can happen as more stocks become available in 2021.</p>
