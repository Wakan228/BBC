				<div class="body_left_main_text">
					<div class="text_head"><p>
						The UK has become the first country in the world to approve the Pfizer/BioNTech coronavirus vaccine, paving the way for mass vaccination.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Britain's medicines regulator, the MHRA, says the jab, which offers up to 95% protection against Covid-19 illness, is safe to be rolled out.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						The first doses are already on their way to the UK, with 800,000 due in the coming days, Pfizer said.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Health Secretary Matt Hancock said the NHS will contact people about jabs.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Elderly people in care homes and care home staff have been placed top of the priority list, followed by over-80s and health and care staff.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						But because hospitals already have the facilities to store the vaccine at -70C, as required, the very first vaccinations are likely to take place there - for care home staff, NHS staff and patients - so none of the vaccine is wasted.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						A further 648 deaths within 28 days of a positive Covid-19 test were recorded in the UK on Wednesday, with another 16,170 cases reported</p>
					</div>
				</div>
								<div class="body_left_main_text">
					<div class="text_center"><p>
						Prime Minister Boris Johnson urged the public not to get "carried away with over optimism or falling into the naive belief that our struggle is over".</p>
					</div>
				</div>
								<div class="body_left_main_text">
					<div class="text_center"><p>
						He told <a href="https://www.bbc.co.uk/news/uk-55166292" class="red_link">a Downing Street news conference that,</a> while the "searchlights of science" had created a working vaccine, significant logistical challenges remained.</p>
					</div>
				</div>
					<div class="body_left_main_text">
					<div class="text_center">
						<ul>
							<li><a href="" class="red_link">What are the safety checks for vaccines</a></li>
							<li><a href="" class="red_link">How will I get a vaccine?</a></li>
							<li><a href="" class="red_link">NHS staff: 'Vaccine is a game changer'</a></li>
						</ul>
					</div>
