					<div class="text_center"><p>
						There is a clear priority list for who gets the vaccine first - and care home residents and staff are top of it.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						But operational complexities mean the reality will be somewhat different.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						When the vaccines arrives, it will be sent straight to major hospitals who have the ultra-cold facilities to store it.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						From there it can be moved just once - and when it is, it must be kept in batches of 1,000.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						That means sending it out to care homes, where there may be only a few dozen residents in some places, would lead to a huge amount of vaccine being wasted.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Because of that, the NHS, which is in charge of distributing the vaccine, will run clinics from hospitals at first.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						This will allow NHS and care home staff to get immunised first as well as, perhaps, some of the older age groups who come into hospital.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						It looks like it will not be until much more of the Pfizer vaccine is available or the Oxford University one, which is easier to distribute, is approved that care home residents will be able to get it.</p>
					</div>
				</div>
				<div class="greey_line"></div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						From there it can be moved just once - and when it is, it must be kept in batches of 1,000.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						While Mr Hancock said that the government does not yet know how many people need to be vaccinated before restrictions can start being lifted, he added: "I'm confident now, with the news today, that from spring, from Easter onwards, things are going to be better. And we're going to have a summer next year that everybody can enjoy."</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
					Mr Johnson added: "It's the protection of vaccines that will ultimately allow us to reclaim our lives and get the economy moving again."</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Downing Street press secretary Allegra Stratton said Mr Johnson would not rule out receiving the vaccine jab live on television, though she said he would not want to take a jab meant for someone more vulnerable.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						The free vaccine will not be compulsory and there will be three ways of vaccinating people across the UK:</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center">
						<ul>
							<li>Hospitals</li>
							<li>Vaccination centres "a bit like the Nightingales project and including some of the Nightingales", said Mr Hancock</li>
							<li>In the community, with GPs and pharmacists</li>
						</ul>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						Around 50 hospitals are on stand-by and vaccination centres - in venues such as conference centres or sports stadiums - are being set up now.</p>
					</div>
				</div>
				<div class="body_left_main_text">
					<div class="text_center"><p>
						It is thought the vaccination network could start delivering more than one million doses a week once enough doses are available.</p>
					</div>
